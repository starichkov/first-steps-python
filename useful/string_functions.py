# Useful string functions
hw = "Hello, World!"

# join and split
print(", ".join(["Hello", "World"]))
print(hw.split(","))

# replace
print(hw.replace("!", "!!!"))

# lower and upper
print(hw.lower())
print(hw.upper())

# startswith and endswith
print(hw.startswith("Hell"))
print(hw.startswith("Hi"))
print(hw.endswith("!"))
print(hw.endswith("?"))
